package com.org.mcq;

public class Answer {
	int id;
	int answer;
	public Answer(int id, int answer) {
		super();
		this.id = id;
		this.answer = answer;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAnswer() {
		return answer;
	}
	public void setAnswer(int answer) {
		this.answer = answer;
	}
	
	
}
